static inline void draw_char(Canvas* canvas, int x, int y, char c) {
    if((c < 0x20) || (c > 0x7E)) {
        return;
    }

    const uint8_t* glyph = FONT5X3[c - 0x20];
    for(int row = 0; row < 5; row++) {
        for(int col = 0; col < 3; col++) {
            if(glyph[row] & (0b100 >> col)) {
                canvas_draw_dot(canvas, x + col, y + row);
            }
        }
    }
}
